   public boolean validates()
  {
      if(txtUname.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(LoginUI.this, "Enter uname");
          txtUname.grabFocus();
          return false;
      }
      if(txtPass.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(LoginUI.this, "Enter password");
          txtPass.grabFocus();
          return false;
      }
      return true;
  }
